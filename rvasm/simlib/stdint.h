#ifndef STDINT_H
#define STDINT_H

/*Signed Double-Word [signed long long]*/
typedef signed long long int64_t;
/*Unsigned Double-Word [unsigned long long]*/
typedef unsigned long long uint64_t;
/*Signed Word [signed int]*/
typedef signed int int32_t;
/*Unsigned Word [unsigned int]*/
typedef unsigned int uint32_t;   
/*Signed Half-Word [signed char]*/
typedef signed short int16_t;
/*Unsigned Half-Word [unsigned short]*/
typedef unsigned short uint16_t;
/*Signed byte [signed char]*/
typedef signed char int8_t;
/*Unsigned byte [unsigned char]*/
typedef unsigned char uint8_t;

#endif